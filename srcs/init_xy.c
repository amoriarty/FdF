#include "fdf.h"

t_xy			*init_xy(void)
{
	t_xy		*res;

	res = (t_xy *)malloc(sizeof(t_xy));
	if (!res)
		return (NULL);
	res->x = 0;
	res->y = 0;
	res->z = 0;
	return (res);
}
